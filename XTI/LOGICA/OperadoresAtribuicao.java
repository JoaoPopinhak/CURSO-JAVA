package br.com.xti.logica;


public class OperadoresAtribuicao { public static void main (String [] args){
	
	int x = 6;
	x = x + 3;
	x += 3; // atalho para operação acima (x = x + 3;)
	
	
	System.out.println();
	}
}

/*

Operadores de atribuição

+= Adição
-= Subtração
*= Mutiplicação
/= Divisão
%= Módulo

*/